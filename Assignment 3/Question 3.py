print("Printing different ranges ")

for i in range(5):
    print(i)
    print("")
    
for i in range(3,10):
    print(i)
    print("")


for i in range(4,13,3):
    print(i)
    print("")

    
for i in range(15,5,-2):
    print(i)
    print("")


for i in range(5,3):
    print(i,)
    print("")



